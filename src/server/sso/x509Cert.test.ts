test("normalizeX509Cert accepts PEM and base64", () => {
  const pem = normalizeX509Cert(MOCK_CERT);
  assert.match(pem, /-----BEGIN CERTIFICATE-----/);
  const oneLine = MOCK_CERT.replace(/-----BEGIN CERTIFICATE-----/g, "")
    .replace(/-----END CERTIFICATE-----/g, "")
    .replace(/\s+/g, "");
  assert.equal(normalizeX509Cert(oneLine).includes("-----BEGIN"), true);
});

test("isValidX509Cert rejects session tokens", () => {
  assert.equal(
    isValidX509Cert("coop_sess_57c1cb1013ffa96c9f08fad13ab0c6344a8148515c24ecf4772a7b397ae66541"),
    false
  );
  assert.equal(isValidX509Cert(MOCK_CERT), true);
});
